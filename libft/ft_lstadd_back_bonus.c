#include "libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*sup;

	if (lst && new)
	{
		if (!lst[0])
			lst[0] = new;
		else
		{
			sup = lst[0];
			while (sup->next)
				sup = sup->next;
			sup->next = new;
		}
	}
}
